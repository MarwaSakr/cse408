public class ColorFeature {
    int x;
    int y;
    int colorRepresentative;
    int numPixels;

    public ColorFeature(int x, int y, int colorRepresentative, int numPixels){
        this.x = x;
        this.y = y;
        this.colorRepresentative = colorRepresentative;
        this.numPixels = numPixels;

    }
}
